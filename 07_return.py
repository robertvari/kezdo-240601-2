def add_numbers(a, b):
    return a + b

def divide_numbers(a, b):
    return a / b

def multiply_numbers(a, b):
    return a * b

add_result = add_numbers(20, 8)
divide_result = divide_numbers(add_result, 5)
multiply_result = multiply_numbers(divide_result, 10)

print( add_result )
print( divide_result )
print( multiply_result )